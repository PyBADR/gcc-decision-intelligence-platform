class IndicatorsInput(BaseModel):
    """API request body for macro analysis."""
    brent_crude: float | None = Field(
        default=None,
        description="Brent crude oil price (USD/barrel)",
        json_schema_extra={"example": 82.5},
    )
    interest_rate: float | None = Field(
        default=None,
        description="Central bank policy rate (decimal, e.g. 0.055 = 5.5%)",
        json_schema_extra={"example": 0.055},
    )
    inflation: float | None = Field(
        default=None,
        description="CPI inflation rate (decimal, e.g. 0.035 = 3.5%)",
        json_schema_extra={"example": 0.035},
    )
    gdp_growth: float | None = Field(
        default=None,
        description="GDP growth rate (decimal, e.g. 0.03 = 3%)",
        json_schema_extra={"example": 0.03},
    )
    fx_usd_sar: float | None = Field(
        default=None,
        description="USD/SAR exchange rate",
        json_schema_extra={"example": 3.75},
    )
    credit_growth: float | None = Field(
        default=None,
        description="Credit growth rate (decimal)",
        json_schema_extra={"example": 0.08},
    )
    pmi: float | None = Field(
        default=None,
        description="Purchasing Managers Index",
        json_schema_extra={"example": 54.2},
    )
    real_estate_index: float | None = Field(
        default=None,
        description="Real estate price index (100 = baseline)",
        json_schema_extra={"example": 105.0},
    )
    trade_balance: float | None = Field(
        default=None,
        description="Trade balance (billion USD)",
        json_schema_extra={"example": 15.2},
    )
    govt_spending_growth: float | None = Field(
        default=None,
        description="Government spending growth rate (decimal)",
        json_schema_extra={"example": 0.05},
    )
    vix: float | None = Field(
        default=None,
        description="VIX volatility index",
        json_schema_extra={"example": 18.5},
    )
    shipping_cost_index: float | None = Field(
        default=None,
        description="Shipping cost index (100 = baseline)",
        json_schema_extra={"example": 110.0},
    )

    def to_indicator_dict(self) -> dict[str, float]:
        """Convert to flat dict, excluding None values."""
        return {
            k: v
            for k, v in self.model_dump().items()
            if v is not None
        }


