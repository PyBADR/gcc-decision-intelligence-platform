def compute_macro_audit_hash(
    regime: str,
    signals_count: int,
    risk_overlay: float,
    indicators: dict[str, float],
) -> str:
    """SHA-256 audit hash for macro context."""
    payload = json.dumps({
        "regime": regime,
        "signals_count": signals_count,
        "risk_overlay": round(risk_overlay, 6),
        "indicator_keys": sorted(indicators.keys()),
    }, sort_keys=True)
    return hashlib.sha256(payload.encode()).hexdigest()
